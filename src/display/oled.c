static void oled_cmd(uint8_t cmd) {
    uint8_t b[] = {0x00, cmd};
    i2c_write_blocking(i2c0, ADDR_OLED, b, 2, false);
}

void oled_init() {
    uint8_t init[] = {
        0xAE,       // Display OFF
        0xD5, 0x80, // Set clock divide ratio
        0xA8, 0x3F, // Set multiplex ratio
        0xD3, 0x00, // Set display offset
        0x40,       // Set start line
        0x8D, 0x14, // Enable charge pump
        0x20, 0x02, // Set addressing mode (Page)
        0xA1,       // Set segment re-map
        0xC8,       // Set COM output scan direction
        0xAF        // Display ON
    };
    for(int i=0; i<sizeof(init); i++) oled_cmd(init[i]);
}

void oled_write_char(char c, uint8_t page, uint8_t col) {
    if (c < 32 || c > 90) c = 32; 
    const uint8_t *glyph = &font[(c - 32) * 5];
    
    oled_cmd(0xB0 + page);
    oled_cmd(0x00 + (col & 0x0F));
    oled_cmd(0x10 + ((col >> 4) & 0x0F));
    
    uint8_t buf[6];
    buf[0] = 0x40; // Data control byte
    memcpy(&buf[1], glyph, 5);
    i2c_write_blocking(i2c0, ADDR_OLED, buf, 6, false);
}

void oled_write_str(const char* str, uint8_t page, uint8_t col) {
    while (*str) {
        oled_write_char(*str++, page, col);
        col += 6; 
        if (col > 122) break;
    }
}

void oled_clear() {
    for (int p = 0; p < 8; p++) {
        oled_cmd(0xB0 + p);
        oled_cmd(0x00);
        oled_cmd(0x10);
        
        uint8_t buffer[129] = {0};
        buffer[0] = 0x40; // Data control byte
        i2c_write_blocking(i2c0, ADDR_OLED, buffer, 129, false);
    }
}